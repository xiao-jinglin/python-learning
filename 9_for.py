"""
問題：条件付き合計（シンプル・難）
次のリストがあります。
numbers = [5, 12, 7, 20, 3, 18, 1]
要求
for文を使う
10以上の数値だけを合計する
最終的な合計を表示する
"""
# 数値のリスト
numbers = [5, 12, 7, 20, 3, 18, 1]

# 合計を保存する変数を0で初期化
total = 0

# リストの中身を1つずつ取り出す
for n in numbers:
    # 10以上の数値かどうかを判定
    if n >= 10:
        # 条件を満たす場合だけ合計に加える
        total += n

# 最終的な合計を表示
print("合計：", total)
