"""
問題：成績の判定（リスト応用・ループなし）

条件
ユーザーに 3科目の点数 を入力させる
点数を リスト に保存する
次の内容を判定して表示する
判定内容

平均点を表示する
すべて60点以上 → 合格
1つでも60点未満 → 不合格
90点以上が1つでもある → 優秀
"""
# 点数を保存するための空のリストを作成
scores = []

# 1科目目の点数を入力
s1 = int(input("1科目目の点数を入力してください："))
scores.append(s1)

# 2科目目の点数を入力
s2 = int(input("2科目目の点数を入力してください："))
scores.append(s2)

# 3科目目の点数を入力
s3 = int(input("3科目目の点数を入力してください："))
scores.append(s3)

# 合計点を求める
total = sum(scores)

# 平均点を計算する
average = total / 3

# 平均点を表示
print("平均点：", average)

# すべて60点以上かどうかを判定
# min() はリストの中で一番小さい値を返す
if min(scores) >= 60:
    print("結果：合格")
else:
    print("結果：不合格")

# 90点以上の点数があるかどうかを判定
# max() はリストの中で一番大きい値を返す
if max(scores) >= 90:
    print("評価：優秀")
